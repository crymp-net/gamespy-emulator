int end(int status) {
    printf("%d", status);
    fflush(stdout);
    return status;
}

time_t time_ms() {
    struct timespec tp;
    clock_gettime(CLOCK_MONOTONIC, &tp);
    return tp.tv_sec * 1000 + tp.tv_nsec / 1000000;
}

int main(int argc, const char **argv) {
    if(argc < 3) return end(-100);
    char data[2000];
    struct sockaddr_in sa, ca;
    struct timeval tv;
    socklen_t cl = sizeof(ca);
    const char* ip = argv[1];
    int port = atoi(argv[2]);
    int fd;
    ssize_t resp;
    time_t rtt_start, rtt_end;
    memset(&sa, 0, sizeof(sa));
    sa.sin_family = AF_INET;
    sa.sin_port = htons(port);
    if(inet_aton(ip, &sa.sin_addr) < 0) {
        return end(-1);
    }
    fd = socket(AF_INET, SOCK_DGRAM, IPPROTO_UDP);
    if(fd < 0) {
        return end(-2);
    }

    tv.tv_sec = 1;
    tv.tv_usec = 0;
    setsockopt(fd, SOL_SOCKET, SO_RCVTIMEO, (const char*)&tv, sizeof tv);
    setsockopt(fd, SOL_SOCKET, SO_SNDTIMEO, (const char*)&tv, sizeof tv);

    rtt_start = time_ms();
    if(sendto(fd, join, sizeof(join), 0, (struct sockaddr*)&sa, sizeof(sa)) < 0) {
        close(fd);
        return end(-3);
    }

    resp = recvfrom(fd, data, sizeof(data), 0, (struct sockaddr*)&ca, &cl);
    if(resp < 0) {
        close(fd);
        return end(-4);
    }
    rtt_end = time_ms();

    printf("%d", (int)(rtt_end - rtt_start));
    return 0;
}